import java.util.*;
public class traversal_using_get {
    public static void main(String[]args){
        List<String> al=new ArrayList<String>();
        al.add("gfg");
        al.add("ide");
        al.add("courses");
        for(int i=0;i<al.size();i++)
        {
            System.out.println(al.get(i));
        }

    }
}
